
public class Point {

	
   private double x0; //variables for coordinates of points
   private double y0;
   private double x;
   private double y;

    void setX0(double coorX0){ //getters and setters for coordinates

    	x0 = coorX0;

    }

    void setX(double coorX){

    	x = coorX;

    }

    void setY0(double coorY0){

    	y0 = coorY0;

    }

    void setY(double coorY){

    	y = coorY;

    }

    double getX0(){

    	return x0;

    }

    double getX(){

    	return x;

    }

    double getY0(){

    	return y0;

    }

    double getY(){

    	return y;

    }
}
